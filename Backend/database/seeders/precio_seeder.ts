export default class extends BaseSeeder {
  async run() {
    await Producto.query().delete()
    await Zona.query().delete()

    for (const [nombreZona, filas] of Object.entries(DATOS)) {
      const zona = await Zona.create({ nombre: nombreZona })
      const productos = filas.map((f: any, i: number) => ({
        zonaId: zona.id,
        grupo: f.grupo,
        producto: f.producto || '',
        und: f.und,
        precio: f.precio,
        factor: f.factor,
        orden: i,
      }))
      await Producto.createMany(productos)
    }
    console.log('Seeder completado: ' + Object.keys(DATOS).length + ' zonas, ' + Object.values(DATOS).reduce((a: number, b: any[]) => a + b.length, 0) + ' productos')
  }
}